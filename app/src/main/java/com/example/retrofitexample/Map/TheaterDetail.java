package com.example.retrofitexample.Map;

public class TheaterDetail {

    String theaterName;
    String theaterPosterImg;
    String theaterTelNum;
    String theaterSeats;
    String movieName[];
    String movieTime[];
    String url;

    public String getUrl() {
        return url;
    }

    public String getTheaterName() {
        return theaterName;
    }

    public String getTheaterPosterImg() {
        return theaterPosterImg;
    }

    public String getTheaterTelNum() {
        return theaterTelNum;
    }

    public String getTheaterSeats() {
        return theaterSeats;
    }

    public String[] getMovieName() {
        return movieName;
    }

    public String[] getMovieTime() {
        return movieTime;
    }

}
